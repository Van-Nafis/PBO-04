public class lingkaran {
    //Luas
    static double hitungLuas(int r){
        return Math.PI * r *r;
    }
    
    static double hitungLuas(double r){
        return Math.PI * r *r;
    }
    
    
    //Keliling
    static double hitungKeliling(int r){
        return 2 * Math.PI * r;
    }
    
    static double hitungKeliling(double r){
        return 2 * Math.PI * r;
    }
}
